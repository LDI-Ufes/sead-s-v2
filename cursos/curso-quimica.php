        <h2 id='perfil-do-egresso'>Perfil do egresso</h2>
        <p><strong>No âmbito profissional:</strong>
        <br>• ter consciência da importância social da profissão como possibilidade de desenvolvimento social coletivo;
        <br>• ter capacidade de disseminar e utilizar o conhecimento químico para a comunidade;
        <br>• atuar no ensino da Educação Básica, utilizando metodologia de ensino variada;
        <br>• contribuir para despertar o interesse científico e o desenvolvimento intelectual do educando;
        <br>• organizar e utilizar laboratórios de química;
        <br>• produzir e analisar criticamente livros didáticos e paradidáticos e indicar bibliografia para o ensino da Química;
        <br>• analisar e elaborar programas para a Educação Básica;
        <br>• exercer a sua profissão com espírito dinâmico, criativo, na busca de novas alternativas educacionais, enfrentando como desafio as dificuldades da docência;
        <br>• conhecer criticamente os problemas educacionais brasileiros;
        <br>• identificar no contexto da realidade escolar os fatores determinantes do processo educativo, tais como o contexto socioeconômico, a política educacional, a administração escolar e os fatores específicos do processo ensino-aprendizagem de Química;
        <br>• assumir conscientemente a tarefa educativa, cumprindo o papel social de preparar os alunos para o exercício consciente da cidadania.</p>

        <p><strong>No âmbito do ensino de Química:</strong>
        <br>• refletir de forma crítica acerca de sua prática docente, identificando problemas de ensino e aprendizagem;
        <br>• compreender e avaliar criticamente os aspectos sociais, tecnológicos, ambientais, políticos e éticos relacionados às aplicações da química na sociedade;
        <br>• saber trabalhar em laboratório e saber usar a experimentação em química como recurso didático;
        <br>• possuir conhecimentos básicos do uso de computadores e sua aplicação em ensino de Química;
        <br>• possuir conhecimentos dos procedimentos e normas de segurança no trabalho;
        <br>• conhecer teorias psicopedagógicas que fundamentam o processo de ensino-aprendizagem, bem como os princípios de planejamento educacional;
        <br>• conhecer os fundamentos, a natureza e as principais pesquisas de ensino de Química;
        <br>• conhecer e vivenciar projetos e propostas curriculares de ensino de Química;
        <br>• ter atitude favorável à incorporação, em sua prática, dos resultados da pesquisa educacional em ensino de Química, visando a solucionar os problemas relacionados ao ensino-aprendizagem.
        </p>

