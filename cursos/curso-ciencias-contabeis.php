        <h2 id='perfil-do-egresso'>Perfil do egresso</h2> 

        <p>O curso de Ciências Contábeis deve levar o egresso à:</p>
        <ol type="I">
          <li>Compreender as questões científicas, técnicas, sociais, econômicas e financeiras, em âmbito nacional e internacional e nos diferentes modelos de organização;</li>
          <li>Apresentar pelo domínio das responsabilidades funcionais envolvendo apurações, auditorias, perícias, arbitragens, noções de atividades atuariais e de quantificações de informações financeiras, patrimoniais e governamentais, com a plena utilização de inovações tecnológicas;</li>
          <li>Revelar capacidade crítico-analítica de avaliação, quanto às implicações organizacionais com o advento da tecnologia da informação. (art. 3º - Res. 10/2004 – CFE/CNE).</li>
        </ol>
        <p>Por tanto, o egresso deve ser portador de um perfil que contemple amplos conhecimentos que lhe proporcione formação científica e capacitação técnica na área de atuação. Além do que, é preciso que o egresso seja portador de uma sólida formação humanística, de forma a estar preparado para enfrentar as demandas no campo empresarial e social, materializando os conhecimentos de modo interdisciplinar. Além do que, espera-se que o egresso tenha um perfil de pesquisador capaz de contribuir decisiva e permanentemente para o contínuo aperfeiçoamento das Ciências Contábeis no que diz respeito aos aspectos de identificação e geração de informações econômicas, financeiras e sociais, de modo que o desempenho do profissional possibilite melhorar cada vez mais a qualidade de vida da sociedade brasileira bem como a competitividade dos agentes Sociais e/ou econômicos nos quais prestam seus serviços.</p>
        <p>E, uma vez que o Currículo do Curso em questão está de acordo com o que estabelecem as Diretrizes Curriculares Nacionais, espera-se que o egresso seja portador das seguintes competências e habilidades:</p>
        <ol type="I">
          <li>utilizar adequadamente a terminologia e a linguagem das Ciências Contábeis e Atuariais;</li>
          <li>demonstrar visão sistêmica e interdisciplinar da atividade contábil;</li>
          <li>elaborar pareceres e relatórios que contribuam para o desempenho eficiente e eficaz de seus usuários, quaisquer que sejam os modelos organizacionais;</li>
          <li>aplicar adequadamente a legislação inerente às funções contábeis;</li>
          <li>desenvolver, com motivação e através de permanente articulação, a liderança entre equipes multidisciplinares para a captação de insumos necessários aos controles técnicos, à geração e disseminação de informações contábeis, com reconhecido nível de precisão;</li>
          <li>exercer suas responsabilidades com o expressivo domínio das funções contábeis, incluindo noções de atividades atuariais e de quantificações de informações financeiras, patrimoniais e governamentais, que viabilizem aos agentes econômicos e aos administradores de qualquer segmento produtivo ou institucional o pleno cumprimento de seus encargos quanto ao gerenciamento, aos controles e à prestação de contas de sua gestão perante a sociedade, gerando também informações para a tomada de decisão, organização de atitudes e construção de valores orientados para a cidadania.</li>
          <li>desenvolver, analisar e implantar sistemas de informação contábil e de controle gerencial, revelando capacidade crítico analítica para avaliar as implicações organizacionais com a tecnologia da informação;</li>
          <li>exercer com ética e proficiência as atribuições e prerrogativas que lhe são prescritas através da legislação específica, revelando domínios adequados aos diferentes modelos organizacionais”. (Resolução Nº 10/2004 – CFE/CNE).</li>
        </ol>
        <p>Desta forma espera-se que o egresso do Curso de Ciências Contábeis da UFES na modalidade em EAD disponha de vasta base cultural; tenha ampla visão das tendências sociais e do mercado; possua facilidade de expressão, espírito empreendedor, liderança e ética em todas as suas atividades profissionais. Espera-se, também, que o profissional seja ágil diante das mudanças de mercado e sempre receptivo às inovações; seja um ser humano público consciente dos seus deveres e direitos; com plena capacidade de ser solidário, de dialogar com profissionais de outras áreas e de participar com responsabilidade e competência do processo de integração e desenvolvimento social, político e econômico, além de ser um profissional completamente familiarizado à nova realidade mundial e capaz de saber adaptar as condições locais às de uma nova ordem internacional.</p> 
 
